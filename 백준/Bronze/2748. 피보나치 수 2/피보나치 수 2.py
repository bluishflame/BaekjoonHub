# 사용자로부터 n을 입력받는다. (n은 90 이하의 자연수)
n = int(input())

# 피보나치 수열을 저장할 리스트를 초기화한다. 
# 피보나치 수열의 첫 번째와 두 번째 값은 각각 0과 1이다.
fibo = [0, 1]

# n번째 피보나치 수를 저장할 변수를 초기화한다.
answer = 0

# 첫 번째 수(0)와 두 번째 수(1)를 이미 알고 있으므로,
# n-1번 반복하면서 피보나치 수를 계산한다.
for _ in range(n - 1):
    # 피보나치 수열의 점화식: F(n) = F(n-1) + F(n-2)
    answer = fibo[0] + fibo[1]

    # 이전 두 값을 갱신한다.
    # 기존의 두 번째 값을 첫 번째 값으로 이동
    fibo[0] = fibo[1]

    # 계산한 새로운 값을 두 번째 값으로 이동
    fibo[1] = answer

# 만약 n이 1이면 1을 출력 (기본 리스트로 인해 처리되지 않기 때문)
if n == 1:
    print(1)
else:
    # n번째 피보나치 수를 출력
    print(answer)
